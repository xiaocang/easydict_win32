    pub fn from_iso639(code: &str) -> Self {
        match code.trim().to_ascii_lowercase().as_str() {
            "unknown" | "" => Self::English,
            other => {
                let language = Self::from_code(other);
                if language == Self::Auto && other != "auto" {
                    Self::English
                } else {
                    language
                }
            }
        }
    }
